SELECT GB.TITLE , GB.BOARD_ID , GR.REPLY_ID ,GR.WRITER_ID , GR.CONTENTS , 
DATE_FORMAT(GR.CREATED_DATE , '%Y-%m-%d') AS CREATED_DATE
FROM USED_GOODS_BOARD GB
JOIN USED_GOODS_REPLY GR ON GB.BOARD_ID = GR.BOARD_ID
WHERE YEAR(GB.CREATED_DATE) =2022 AND MONTH(GB.CREATED_DATE) = 10
ORDER BY GR.CREATED_DATE ASC , GB.TITLE ASC;